

<h1> Über uns </h1>


<p>Wir sind vier Studenten der technischen Hochschule in Brandenburg und stehen für ein nachhaltiges Kochereignis für Jedermann.
   Gemäß der Einstellung Zero Waste, wollen wir auch in unseren alltäglichen Küchenschlachten keine Lebensmittel verschwenden und haben uns daher entschlossen eine Lösung zu entwickeln.
   Wir kämpfen an gegen die Lebensmittelverschwendung in Form ungenutzter Reste in der Küche, Ahnungslosigkeit in der Küche und fehlender Kreativität durch Kochlaien.</p>
   </br>
<h2>   Unsere Lösung:</h2>
<p>Ein Internetportal mit Zugriff auf sämtliche Rezepte und deren Anleitungen, welche das Herz begehrt.
   Rezepte können von den Mitgliedern der Zero-Waste-Community erstellt und über Social Media geteilt werden, um eine Interaktion der Mitglieder zu ermöglichen.</p>
   </br>




